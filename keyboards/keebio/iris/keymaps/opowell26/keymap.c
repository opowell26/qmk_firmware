bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) { 
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
   case GAMING:
      if (record->event.pressed) {
         set_single_persistent_default_layer(_GAMING);
      }
      return false;
      break;
   case MAC: 
      if (record->event.pressed) {
         set_single_persistent_default_layer(_MAC);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
   case MLOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_MLOWER, _MRAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_MLOWER, _MRAISE, _ADJUST);
      }
      return false;
      break;
   case MRAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_MLOWER, _MRAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_MLOWER, _MRAISE, _ADJUST);
      }
      return false;
      break;
   case ADJUST:
      if (record->event.pressed) {
         layer_invert(_ADJUST);
         update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
         layer_off(_ADJUST);
         update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
  }
  return true;
}
